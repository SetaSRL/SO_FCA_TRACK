    private Color getColor(String stato) {
        switch (stato) {
            case "CH" :
            case "NL" :
                return Color.RED;                
            case "SO" :
                return Color.ORANGE;
            default:
             return Color.GREEN;
        }
    };
    
    private void loadPratica() {
        
        this.pratica = this.db.getPraticaDaLavorare();
        num_pra = this.pratica.getNum_pra();
        if (null == num_pra) {
            String msg = "Nessuna pratica da lavorare";
            this.jLabelEsito.setText(msg);
            return;
        }
        id_pratica = this.pratica.getIdPratica().toString();
        codicePratica.setText(num_pra);
        this.lsLav = this.db.VerificaPratica(num_pra);
        String Esito = "";
        if (!this.lsLav.isEmpty()) {             
            Lavorazione pl = this.lsLav.get(0);
            Esito = pl.getEsito();
            this.tabellaLavorazioni.setModel(getParModelU1());
        } else {
            DefaultTableModel model = (DefaultTableModel) this.tabellaLavorazioni.getModel();
            model.setRowCount(0);
        }           
        circleColor = getColor(Esito);
        this.jPanel2.setVisible(true);
        this.saveEsito.setEnabled(true);
    }
    
     public DefaultTableModel getParModelU1() {
        try {
             DefaultTableModel defaultTableModel = new DefaultTableModel() {

                @Override
                public boolean isCellEditable(int rowIndex, int mColIndex) {
                    return false;
                }
            };
            defaultTableModel.addColumn("ESITO");
            defaultTableModel.addColumn("DATALAV");
            defaultTableModel.addColumn("OPERATORE");
            defaultTableModel.addColumn("NOTA");
            
           
            int i = 0;
            this.lsLav.forEach(lav ->{
                defaultTableModel.insertRow(i, new Object[]{lav.getEsito(),lav.getDataLavorazione(),lav.getUtente(),lav.getNota()});
            });
            return defaultTableModel;
        } catch (Exception ex) {
            Logger.getLogger(ex.getMessage());
        }
        return null;
    }

